namespace lochlite\cms\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use lochlite\cms\Models\Page;

class Pagesbody extends Model
{
    use HasFactory;
		
    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = ['page_id', 'body'];	

    /**
     * Get the corresponding post details..
     */
    public function page()
    {
        return $this->belongsTo(Page::class, 'page_id', 'id')->first();
    }
}
